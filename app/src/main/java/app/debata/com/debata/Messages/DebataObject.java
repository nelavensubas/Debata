package app.debata.com.debata.Messages;

/**
 * DebataObject is the class that will contain the debate value.
 *
 * @author Nelaven Subaskaran
 * @since 1.0
 */
public class DebataObject {
    private String debate;

    public DebataObject(String debate) {
        this.debate = debate;
    }

    public String getDebate() {
        return debate;
    }

    public void setDebate(String debate) {
        this.debate = debate;
    }
}
